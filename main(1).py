nums = list(map(int, input().strip().split(' ')))
supposed = int(input())

nums_indices = {}
for i, num in enumerate(nums):
    nums_indices[num] = i

nums_ranges = []

for num in nums:
    complement = supposed - num
    if complement in nums_indices and nums_indices[complement] != nums_indices[num]:
        nums_ranges.extend(sorted([num, complement]))

if nums_ranges:
    indices_sum = [nums_indices[num] + nums_indices[complement] for num, complement in zip(nums_ranges[0::2], nums_ranges[1::2])]
    indices_sum.sort()

    for index_sum in range(0,len(indices_sum),2):
        print(indices_sum[index_sum])
else:
    print('Not Found!')
